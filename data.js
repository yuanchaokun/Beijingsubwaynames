function getStationsByLine(lineName) {
    return stationData.filter(station => station.lines.includes(lineName));
}

function searchStations(query) {
    const lowerQuery = query.toLowerCase();
    return stationData.filter(station => 
        station.name.toLowerCase().includes(lowerQuery)
    );
}

function getRandomStation() {
    const randomIndex = Math.floor(Math.random() * stationData.length);
    return stationData[randomIndex];
}

function getStationById(id) {
    return stationData.find(station => station.id === id);
}

function getStationByName(name) {
    return stationData.find(station => station.name === name);
}

function getNextStation(currentStation, lineName) {
    const lineStations = getStationsByLine(lineName);
    const currentIndex = lineStations.findIndex(station => station.id === currentStation.id);
    
    if (currentIndex === -1) return null;
    
    const nextIndex = (currentIndex + 1) % lineStations.length;
    return lineStations[nextIndex];
}

function getPrevStation(currentStation, lineName) {
    const lineStations = getStationsByLine(lineName);
    const currentIndex = lineStations.findIndex(station => station.id === currentStation.id);
    
    if (currentIndex === -1) return null;
    
    const prevIndex = currentIndex === 0 ? lineStations.length - 1 : currentIndex - 1;
    return lineStations[prevIndex];
}
